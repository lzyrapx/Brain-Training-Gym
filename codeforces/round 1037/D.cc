void solve() {
    int n, k;
    cin >> n >> k;
    vector<int>l(n), r(n), real(n);
    for (int i = 0; i < n; i++) {
        cin >> l[i] >> r[i] >> real[i];
    }
    vector<int>p(n);
    iota(p.begin(), p.end(), 0);
    sort(p.begin(), p.end(), [&](int i, int j){
        return real[i] < real[j];
    });
    // debug(p);
    for (auto x: p) {
        if (l[x] <= k && k < real[x]) {
            k = real[x];
        }
    }
    cout << k << endl;
} 

